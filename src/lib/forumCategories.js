/**
 * Group forums by category using the canonical order.
 * Skips 'start' category and "Read This First" forum.
 * For drug category, takes top forums by post_count (up to drugLimit).
 */
export function groupForums(forums, { drugLimit = 0 } = {}) {
  const grouped = {};
  for (const forum of forums) {
    const cat = forum.category;
    if (cat === 'start') continue;
    if (forum.name?.toLowerCase().includes('read this first')) continue;
    if (!grouped[cat]) grouped[cat] = [];
    grouped[cat].push(forum);
  }

  // Sort drug forums by post count
  if (grouped.drug) {
    grouped.drug.sort((a, b) => (b.post_count || 0) - (a.post_count || 0));
    if (drugLimit > 0) {
      grouped.drug = grouped.drug.slice(0, drugLimit);
    }
  }

  return FORUM_CATEGORY_ORDER
    .map(({ key, label }) => ({
      key,
      label,
      forums: grouped[key] || [],
    }))
    .filter((s) => s.forums.length > 0);
}
