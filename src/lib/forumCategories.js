/**
 * Build the general (non-drug) forum sections for display.
 * Merges hardcoded GENERAL_FORUMS with DB data (for post_count / id).
 */
export function getGeneralSections(dbForums) {
  const dbBySlug = {};
  for (const f of dbForums) {
    if (f.slug) dbBySlug[f.slug] = f;
  }

  return FORUM_CATEGORY_ORDER
    .filter((c) => c.key !== 'drug')
    .map(({ key, label }) => ({
      key,
      label,
      forums: GENERAL_FORUMS
        .filter((f) => f.category === key)
        .map((f) => ({
          ...f,
          id: dbBySlug[f.slug]?.id || f.slug,
          post_count: dbBySlug[f.slug]?.post_count || 0,
        })),
    }))
    .filter((s) => s.forums.length > 0);
}

/**
 * Group drug forums (category='drug') by their drug class,
 * using the DRUG_CATEGORY_GROUPS ordering.
 */
export function getDrugClassGroups(dbForums) {
  const drugForums = dbForums.filter((f) => f.category === 'drug');

  // Map drug_slug → drug class
  const slugToClass = {};
  for (const [slug, drug] of Object.entries(DRUGS)) {
    slugToClass[slug] = drug.class;
  }

  // Group forums by DRUG_CATEGORY_GROUPS key
  const byGroupKey = {};
  for (const forum of drugForums) {
    const drugClass = slugToClass[forum.drug_slug] || 'Other';
    const groupKey = DRUG_CATEGORY_GROUPS.find((g) => g.classes.includes(drugClass))?.key || 'Other';
    if (!byGroupKey[groupKey]) byGroupKey[groupKey] = [];
    byGroupKey[groupKey].push(forum);
  }

  return DRUG_CATEGORY_GROUPS.map((group) => ({
    ...group,
    forums: (byGroupKey[group.key] || []).sort((a, b) => (b.post_count || 0) - (a.post_count || 0)),
  }));
}
